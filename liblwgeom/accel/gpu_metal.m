/**
 * Shut down the Metal backend and release all resources.
 */
void
lwgpu_metal_shutdown(void)
{
	@autoreleasepool
	{
		mtl_pipeline_rad_convert = nil;
		mtl_pipeline_rotate_z_m_epoch = nil;
		mtl_pipeline_rotate_z = nil;
		mtl_library = nil;
		mtl_queue = nil;
		mtl_device = nil;
	}

	metal_initialized = 0;
	metal_disabled = 0;
	metal_device_name[0] = '\0';
}

/**
 * Return the cached Metal device name string.
 */
const char *
lwgpu_metal_device_name(void)
{
	return metal_device_name;
}

